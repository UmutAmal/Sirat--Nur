import 'package:sirat_i_nur/l10n/app_localizations.dart';

String resolveBundledDuaMeaning(AppLocalizations l10n, int? id) {
  switch (id) {
    case 1:
      return l10n.duaMeaning1;
    case 2:
      return l10n.duaMeaning2;
    case 3:
      return l10n.duaMeaning3;
    case 4:
      return l10n.duaMeaning4;
    case 5:
      return l10n.duaMeaning5;
    case 6:
      return l10n.duaMeaning6;
    case 7:
      return l10n.duaMeaning7;
    case 8:
      return l10n.duaMeaning8;
    default:
      return '';
  }
}
