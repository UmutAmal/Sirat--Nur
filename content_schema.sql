-- Content bootstrap schema for Section 13 live-content rollout.
-- Apply this file before running seed.sql so cloud-first content providers
-- can read from verified public tables with RLS enabled.

create table if not exists public.daily_content (
  id bigint generated by default as identity primary key,
  content_type text not null,
  content_ar text not null,
  content_tr text,
  content_en text,
  reference text not null,
  display_date date not null unique,
  verified_at timestamptz,
  created_at timestamptz not null default timezone('utc', now())
);

alter table public.daily_content
add column if not exists verified_at timestamptz;

create index if not exists daily_content_type_display_date_idx
on public.daily_content (content_type, display_date);

create unique index if not exists daily_content_display_date_unique_idx
on public.daily_content (display_date);

alter table public.daily_content enable row level security;

drop policy if exists "Public read daily content" on public.daily_content;
create policy "Public read daily content"
on public.daily_content
for select
using (true);

