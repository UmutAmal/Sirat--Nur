-- Content bootstrap schema for Section 13 live-content rollout.
-- Apply this file before running seed.sql so cloud-first content providers
-- can read from verified public tables with RLS enabled.

create table if not exists public.duas (
  id bigint generated by default as identity primary key,
  title_tr text,
  title_ar text,
  title_en text,
  text_ar text not null,
  transliteration text,
  text_tr text,
  text_en text,
  audio_url text,
  storage_path text,
  category text not null,
  source text not null,
  verified_at timestamptz not null,
  created_at timestamptz not null default timezone('utc', now())
);

alter table public.duas
add column if not exists storage_path text;

create index if not exists duas_category_idx on public.duas (category);

alter table public.duas enable row level security;

drop policy if exists "Public read duas" on public.duas;
create policy "Public read duas"
on public.duas
for select
using (true);

create table if not exists public.asma_ul_husna (
  id smallint primary key,
  name_ar text not null,
  transliteration text not null,
  meaning_tr text,
  meaning_en text,
  audio_url text,
  storage_path text,
  source text not null,
  verified_at timestamptz not null,
  created_at timestamptz not null default timezone('utc', now())
);

alter table public.asma_ul_husna
add column if not exists storage_path text;

alter table public.asma_ul_husna enable row level security;

drop policy if exists "Public read asma ul husna" on public.asma_ul_husna;
create policy "Public read asma ul husna"
on public.asma_ul_husna
for select
using (true);

create table if not exists public.quran_surahs (
  id bigint generated by default as identity primary key,
  surah_number smallint not null unique,
  name_ar text not null,
  name_tr text,
  name_en text,
  name_transliteration text not null,
  ayah_count smallint not null,
  revelation_type text not null,
  source text not null,
  verified_at timestamptz not null,
  created_at timestamptz not null default timezone('utc', now())
);

create index if not exists quran_surahs_number_idx
on public.quran_surahs (surah_number);

alter table public.quran_surahs enable row level security;

drop policy if exists "Public read quran surahs" on public.quran_surahs;
create policy "Public read quran surahs"
on public.quran_surahs
for select
using (true);

create table if not exists public.quran_ayahs (
  id bigint generated by default as identity primary key,
  surah_id bigint not null references public.quran_surahs(id) on delete cascade,
  ayah_number smallint not null,
  juz_number smallint,
  text_ar text not null,
  text_tr text,
  text_en text,
  audio_url text,
  source text not null,
  verified_at timestamptz not null,
  created_at timestamptz not null default timezone('utc', now()),
  unique (surah_id, ayah_number)
);

alter table public.quran_ayahs
add column if not exists juz_number smallint;

create index if not exists quran_ayahs_surah_number_idx
on public.quran_ayahs (surah_id, ayah_number);

alter table public.quran_ayahs enable row level security;

drop policy if exists "Public read quran ayahs" on public.quran_ayahs;
create policy "Public read quran ayahs"
on public.quran_ayahs
for select
using (true);

create table if not exists public.audio_files (
  id bigint generated by default as identity primary key,
  type text not null,
  title text not null,
  url text,
  storage_path text,
  surah_number smallint,
  duration_seconds integer,
  reciter text,
  language text,
  source text not null,
  verified_at timestamptz not null,
  created_at timestamptz not null default timezone('utc', now())
);

alter table public.audio_files
add column if not exists surah_number smallint;

create index if not exists audio_files_type_idx
on public.audio_files (type);

create unique index if not exists audio_files_quran_surah_unique_idx
on public.audio_files (type, reciter, surah_number);

alter table public.audio_files enable row level security;

drop policy if exists "Public read audio files" on public.audio_files;
create policy "Public read audio files"
on public.audio_files
for select
using (true);

